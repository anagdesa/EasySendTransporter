package com.example.easysendtrans.ScreenHome;

public class ScreenItem {

    int ScreenImg1, ScreenImg2;

    public ScreenItem(int screenImg1, int screenImg2){
        ScreenImg1 = screenImg1;
        ScreenImg2 = screenImg2;
    }

    public void setScreenImg1(int screenImg1) {
        ScreenImg1 = screenImg1;
    }

    public void setScreenImg2(int screenImg2) {
        ScreenImg2 = screenImg2;
    }

    public int getScreenImg1() {
        return ScreenImg1;
    }

    public int getScreenImg2() {
        return ScreenImg2;
    }
}



